    private void showGiayKSK() throws SQLException {
        if (getSoKSK() == null) {
            return;
        }
        ResultSet rs = actKSK.getGiayKSK(getSoKSK());
        rs.next();
        txtSoKSK.setText(getSoKSK());
        txtNgay.setDate(rs.getDate("Ngay"));
        txtHoTen.setText(rs.getString("HoTen"));
        txtNamSinh.setValue(rs.getInt("NamSinh"));
        cboGioiTinh.setSelectedIndex(rs.getInt("GioiTinh"));
        txtSoCMND.setText(rs.getString("SoCMND"));
        txtCapNgay.setDate(rs.getDate("CapNgay"));
        maXP = rs.getString("DiaChi");
        txtDiaChi.setText(actDVHC.getTenDVHC(maXP));
        cboLyDo.setSelectedIndex(actLD.getIndexLyDo(lstLD, rs.getString("LyDoID")));
        quocGiaID = rs.getString("QuocGiaID");
        txtQuocGia.setText(actQG.getNameQuocGia(quocGiaID));
        txtKetQua.setText(rs.getString("KetQua"));
    }

    private void showChiPhi() {
        try {
            String[] header = {"Thu", "Mã số", "Nội dung", "Số lượng", "Đơn giá", "Thành tiền"};
            ResultSet rs = actCP.getListChiPhi(txtSoKSK.getText());
            FillData fillData = new FillData();
            fillData.fillDataJTable(jTable1, header, rs);
            TableCellRenderer tcr;
            tcr = new IntegerRenderer();
            jTable1.getColumnModel().getColumn(3).setCellRenderer(tcr);
            jTable1.getColumnModel().getColumn(4).setCellRenderer(tcr);
            jTable1.getColumnModel().getColumn(5).setCellRenderer(tcr);
            if (jTable1.getRowCount() == 0) {
                jTable1.setAutoResizeMode(1);
            } else {
                jTable1.setAutoResizeMode(0);
            }
        } catch (Exception ex) {
            Logger.getLogger(JDialogGiayDK.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void saveDichVu() throws SQLException {
        ResultSet rs = actGoi.getListGoiGiaVP(lstLD.get(cboLyDo.getSelectedIndex()).getLyDoID());
        while (rs.next()) {
            try {
                ChiPhi chiPhi = new ChiPhi(txtSoKSK.getText(), rs.getString("GiaVPID"), 1, rs.getFloat("GiaTH"));
                actCP.saveChiPhi(chiPhi);
            } catch (Exception ex) {
                Logger.getLogger(JDialogGiayDK.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        showChiPhi();
    }
