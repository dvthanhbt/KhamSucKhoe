    private boolean kiemTra() {
        if (txtNgay.getDate().compareTo(Date.valueOf(LocalDate.now())) > 0) {
            JOptionPane.showMessageDialog(null, "Ngày đăng ký sai!");
            txtNgay.requestFocus();
            return false;
        }
        if (txtHoTen.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Chưa nhập họ và tên!");
            txtHoTen.requestFocus();
            return false;
        }
        if (Integer.parseInt(txtNamSinh.getValue().toString()) > LocalDate.now().getYear()) {
            JOptionPane.showMessageDialog(null, "Năm sinh sai!");
            txtNamSinh.requestFocus();
            return false;
        }
        if (txtCapNgay.getDate().compareTo(Date.valueOf(LocalDate.now())) > 0) {
            JOptionPane.showMessageDialog(null, "Ngày cấp CMND sai!");
            txtCapNgay.requestFocus();
            return false;
        }
        if (maXP.trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Chưa nhập địa chỉ!");
            txtDiaChi.requestFocus();
            return false;
        }
        if (quocGiaID.trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Chưa nhập Quốc gia!");
            txtQuocGia.requestFocus();
            return false;
        }

        return true;
    }

